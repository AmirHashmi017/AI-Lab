import numpy as np
arr1=np.array([[1,2],[3,4]])
#Shape Attribute
print(arr1.shape)
#Ndim Attribute
print(arr1.ndim)
#Size Aatribute
print(arr1.size)
#Item Size Gives size of one element in array
print(arr1.itemsize)
# dtype attribute
print(arr1.dtype)
# astype Function to convert type
print(arr1.astype('float'))
